package io.sahka;

public class CoveredClassCalculator {

    public static int multiple(int a, int b) {
        return a * b;
    }

    public static int divide(int a, int b) {
        return a / b;
    }


    public static int multipleOne(int a, int b) {
        return a * b;
    }

    public static int divideOne(int a, int b) {
        return a / b;
    }

}
